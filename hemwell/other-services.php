		<p>Colonoscopy is a procedure used to examine the colon. It is a flexible fibre-optic tube with a small camera at its tip. It is passed through the anus and guided around the colon. Any abnormalities may be biopsied or removed. The procedure is performed in a day surgery unit under sedation.</p>

		<p>On the day prior to colonoscopy, the bowel must be prepared so it is necessary to have a clear liquid diet and to drink a laxative solution. You will not be able to drive home after the procedure.</p>

		<p>Patients with a family history of colorectal cancer or a personal history of inflammatory bowel disease or polyps, may require regular screening with colonoscopy. Any patient with symptoms of rectal bleeding and bowel problems should undergo this exam.</p>
