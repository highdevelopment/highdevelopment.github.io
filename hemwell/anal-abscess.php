	<h1 class="title smaller">Anal Abscess</h1>
	
	<p>This is a collection of pus in the area around the anus. Infection in the anal region usually arises from small glands inside the anal canal. An abscess in the anal area causes severe constant pain. There is frequently an area of swelling close to the anus but this is not always visible. A fever or feeling of being unwell is common. Discharge of pus from the skin or the anus may occur.  
	</p>
	
	<p>Surgical drainage is usually performed which is most commonly done in the HemWell office or outpatient ambulatory surgery center.</p>

	<p>In some cases an abscess is associated with an anal fistula.</p>

